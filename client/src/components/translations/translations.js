// Helper function to get translation
export function getTranslation(lang, key) {
  const langObj = translations[lang] || translations.en;
  
  // Handle nested keys like 'levels.0.title'
  const keys = key.split('.');
  let value = langObj;
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || translations.en[key] || key;
}

// Get random safety tip in selected language
export function getRandomSafetyTip(lang = 'en') {
  const tips = translations[lang]?.safetyTips || translations.en.safetyTips;
  const randomIndex = Math.floor(Math.random() * tips.length);
  return tips[randomIndex];
}